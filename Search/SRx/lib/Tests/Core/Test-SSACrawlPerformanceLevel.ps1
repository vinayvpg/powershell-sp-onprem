# make sure the the $xFarm has been instantiated
if(-not $xSSA._hasSRx) {
    throw [System.NullReferenceException] "This test requires a valid `$xFarm"
}

if (-not $xSSA._hasSRxCrawlDiagnosticsEx) {
    Write-SRx INFO " * Enabling extended Crawl diagnostics on `$xSSA..." -ForegroundColor DarkCyan
    $xSSA | Enable-SRxCrawlDiag -ExtendedObjects | Out-Null
}

$crawlers = $xSSA._GetCC()
$data = $crawlers | ft ServerName, Name, @{l='Cores';e={$_._Cores}},@{l='MaxThreads';e={$_._MaxThreads}},@{l='MaxThreadsPerHost';e={$_._MaxThreadsPerHost}} -auto
$details = ""
$msgDelim = ""
$n1MsgDelim = "`n     - "

foreach ($cc in $crawlers) { 
    $details += $msgDelim + "  * " + $cc.Name + " [" + $cc.ServerName + "]"
    $details += $n1MsgDelim + "CPU Cores            : " + [string]$cc._Cores
    $details += $n1MsgDelim + "Max Robot Threads    : " + [string]$cc._MaxThreads
    $details += $n1MsgDelim + "Max Threads Per Host : " + [string]$cc._MaxThreadsPerHost
    $msgDelim = "`n"
}

# $xSSA will be in scope
$ruleObject = New-Object PSObject
$ruleObject | Add-Member Name "Test-SSACrawlPerformanceLevel"  
$ruleObject | Add-Member ExpectedValue "INFO"
$ruleObject | Add-Member Category @("operations");  
$ruleObject | Add-Member ActualValue $(
	try {
		(Get-SPEnterpriseSearchService).PerformanceLevel
	} catch {
		-1
	})
$ruleObject | Add-Member Success $($ruleObject.ActualValue -ne -1)
$ruleObject | Add-Member Message $(if($ruleObject.Success){ @{ 
                level = "INFO";
                headline = "Search Service: " + $ruleObject.ActualValue;
                details = $details;
                data = $data;
        	} } else { @{
                level = "Exception";
                headline = "Unexpected: Unable to retrieve the PerformanceLevel from the Search Service";
			} } )

#And then just return the object as the last step…
$ruleObject

